/**
 * @brief 计算数据的CRC8校验值(初始值0xFF)
 * @param data 输入数据指针
 * @param len 数据长度
 * @return CRC8校验值
 * @note 默认初始值0xFF,委托CRC8_ComputeWithInit实现
 *        调用者:
 */
uint8_t CRC8_Compute(const uint8_t *data, uint16_t len)
{
    return CRC8_ComputeWithInit(data, len, 0xFF);
}

/**
 * @brief 计算数据的CRC8校验值(自定义初始值)
 * @param data 输入数据指针
 * @param len 数据长度
 * @param init CRC初始值
 * @return CRC8校验值
 * @note 查表法,字节序为常规(非反射);data为NULL或len=0时返回init
 *        调用者:CRC8_Compute
 */
uint8_t CRC8_ComputeWithInit(const uint8_t *data, uint16_t len,
                             uint8_t init)
{
    if (data == NULL || len == 0) return init;
    uint8_t crc = init;
    for (uint16_t i = 0; i < len; i++) {
        crc = CRC8_TABLE[crc ^ data[i]];                                      //查表:crc异或数据字节作为索引
    }
    return crc;
}

/**
 * @brief 计算数据的CRC16校验值(初始值0x0000)
 * @param data 输入数据指针
 * @param len 数据长度
 * @return CRC16校验值
 * @note 默认初始值0x0000,委托CRC16_ComputeWithInit实现
 *        调用者:
 */
uint16_t CRC16_Compute(const uint8_t *data, uint16_t len)
{
    return CRC16_ComputeWithInit(data, len, 0x0000);
}

/**
 * @brief 计算数据的CRC16校验值(自定义初始值)
 * @param data 输入数据指针
 * @param len 数据长度
 * @param init CRC初始值
 * @return CRC16校验值
 * @note 查表法(多项式0x8005);data为NULL或len=0时返回init
 *        调用者:CRC16_Compute
 */
uint16_t CRC16_ComputeWithInit(const uint8_t *data, uint16_t len,
                               uint16_t init)
{
    if (data == NULL || len == 0) return init;
    uint16_t crc = init;
    for (uint16_t i = 0; i < len; i++) {
        uint16_t idx = ((crc >> 8) ^ data[i]) & 0xFF;                        //CRC高8位异或数据字节作为索引
        crc = (uint16_t)((crc << 8) ^ CRC16_TABLE[idx]);                      //左移8位后与查表值异或
    }
    return crc;
}
